package ar.edu.itba.ss.simulator.utils;

public class Pair {
    public final Double x;
    public final Double y;

    public Pair(Double x, Double y) {
        this.x = x;
        this.y = y;
    }

    public Double getX() {
        return x;
    }

    public Double getY() {
        return y;
    }
}
